public class Dwarf
{
    public String myFirstName;
    public String myLastName;

    public Dwarf()
    {
        myFirstName = "Dwarfy";
        myLastName = "McDwarfFace";
    }

    public Dwarf(String firstname, String lastname)
    {
        myFirstName = firstname;
        myLastName = lastname;
    }
}
